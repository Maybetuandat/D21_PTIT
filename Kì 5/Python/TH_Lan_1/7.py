def xuli():
    n = int(input())
    a = list(map(int, input().split()))
    minn, minn[0] = [0] * n, a[0]
    maxx, maxx[-1] = [0] *n, a[-1]
    for i in range(1, n):
        minn[i] = max(a[i], minn[i-1])
    for i in range(n-2, -1, -1):
        maxx[i] = min(a[i], maxx[i+1])
    dem = 0
    for i in range(1, n-1):
        if minn[i-1] <= a[i] < maxx[i+1]:
            dem += 1
    if a[0] < maxx[1]:
        dem += 1
    if a[-1] >= minn[n-2]:
        dem += 1
    print(dem)
for _ in range(int(input())):
    xuli()