n , m = map(int, input().split())
a= []
minn = 10009
maxx = 0
for i in range(n):
    x = list(map(int, input().split()))
    minn = min(minn, min(x))
    maxx = max(maxx, max(x))
    a.append(x)
somayman = maxx - minn
kt = False
ans = []

for i in range(n):
    for j in range(m):
        if a[i][j] == somayman:
            kt = True
            ans.append("Vi tri [{}][{}]".format(i, j))
if kt == False:
    print("NOT FOUND")
else:
    print(somayman)
    for x in ans:
        print(x)