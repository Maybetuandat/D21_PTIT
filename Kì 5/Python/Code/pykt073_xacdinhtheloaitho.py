n = int(input())
a = []
for _ in range(n):
    a.append(input().strip())
i = 0
theloai = []
while i < len(a):
    s = list(a[i].split())
    if len(s) == 6:
       # xac dinh bai tho luon, 
        j =  i + 2
        while True and j < len(a):
           
           x = list(a[j].split())
           if len(x) == 6:
               j += 2
           else:
               break
        theloai.append(1)
        i = j  
    elif len(s) == 7:
        i += 4
        theloai.append(2)
print(len(theloai))
for x in theloai:
    print(x)
