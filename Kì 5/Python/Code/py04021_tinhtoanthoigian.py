from datetime import *
class Time:
    def __init__(self, ma, ten , vao, ra) -> None:
        self.ma = ma
        self.ten = ten
        self.dtime = datetime.strptime(ra, "%H:%M") - datetime.strptime(vao, "%H:%M")
        
        pass
    def __str__(self) -> str:
        tmp = self.dtime.seconds // 60
        gio = tmp // 60
        phut = tmp % 60
        return "{} {} {} gio {} phut".format(self.ma, self.ten,gio, phut)
    
