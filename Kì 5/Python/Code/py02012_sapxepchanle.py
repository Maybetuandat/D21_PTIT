n = int(input())
a = []
#a = list(map(int, input().split()))
while True :
    l = [int(x) for x in input().split()]
    a += l
    if len(a) == n : break
mangchan = []
mangle = []
checklist = [0]  * n
for i in range(n):
    if a[i] % 2 != 0:
        checklist[i] = 1
        mangle.append(a[i])
    else:
        mangchan.append(a[i])
mangchan.sort()
mangle.sort(key = lambda x  : -x)
indexle = 0
indexchan = 0
for i in checklist:
    if i == 0:
        print(mangchan[indexchan], end = " ")
        indexchan += 1
    else:
        print(mangle[indexle], end = " ")
        indexle += 1
