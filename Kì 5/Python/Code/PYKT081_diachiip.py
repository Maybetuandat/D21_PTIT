def xuli():
    a  = list(map(str, input().split(".")))
    if len(a) != 4:
          return "NO"
    for i in a:
        for j in i:
             if j.isalpha()== True:
                  return "NO"
        if int(i) not in range(0, 256):
             return "NO"
    return "YES"
for _ in range(int(input())):
       print(xuli())