import math                       
def convert(s):
    sum = 0
    n = len(s)
    for i in  range(n):
        sum += int(s[i])     *  (1<< (n - i - 1))
    if sum >= 10:
        return chr(sum - 10 + ord('A'))
    return str(sum)

