while True:
    n = int(input())
    if n == 0:
        break
    minn = int(input())
    maxx = minn
    for i in range(n-1):
        x = int(input())
        maxx = max(maxx, x)
        minn = min(minn, x)
    if maxx == minn:
        print("BANG NHAU")
    else:
        print(minn, maxx)