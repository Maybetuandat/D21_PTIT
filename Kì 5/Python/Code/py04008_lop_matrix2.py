s = ""
while True:
   try:
     s += input() + "     "
   except: 
      break
x = list(map(int, s.split()))
t = x[0]
index = 1
for _ in range(t):
    n = x[index]
    index += 1
    m = x[index]
    index += 1
    a = [[0] * (m ) for _ in range(n )]
    dem = 0
    for i in range(n):
       for j in range(m):
            a[i][j] = x[index]
            index += 1
       if dem == n * m:
            break
    b = []
    for i in range(m):
            c  = []
            for  j in range(n):
                c.append(a[j][i])
            b.append(c)
    for i in range(n):
            for j in range(n):
                s = 0
                for k in range(m):
                    s += a[i][k] * b[k][j]
                print(s, end = " ")
            print()
