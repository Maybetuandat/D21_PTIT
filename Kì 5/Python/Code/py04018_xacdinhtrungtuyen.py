from decimal import *                          
uutien = [2.0, 1.5, 1.0, 0.0]                                
class TrungTuyen:
       def __init__(self, tengv, ma, diemtin, diemkhac, i) -> None:
              self.tengv = tengv
              self.magv = "GV" + str(i).zfill(2)
              if ma[0] == 'A':
                     self.monhoc = "TOAN"
              if ma[0] == 'B':
                     self.monhoc = "LY"
              if ma[0] == 'C':
                     self.monhoc = "HOA"
              
              self.tongdiem = Decimal(diemtin) * 2 + Decimal(diemkhac) + Decimal(uutien[int(ma[1]) - 1])
              if self.tongdiem >= 18:
                     self.trangthai = "TRUNG TUYEN"
              else:
                     self.trangthai = "LOAI"
              
              self.tongdiem = self.tongdiem.quantize(Decimal("0.0"), rounding = ROUND_HALF_UP )
       def __str__(self) -> str:
              return "{} {} {} {} {}".format(self.magv, self.tengv, self.monhoc, self.tongdiem, self.trangthai)
a = []
for i in range(int(input())):
       a.append(TrungTuyen(input(), input(), input(), input(), i + 1))
a.sort(key = lambda x : -x.tongdiem)
for x in a:
       print(x)