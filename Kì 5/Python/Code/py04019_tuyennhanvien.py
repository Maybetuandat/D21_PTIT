from decimal import *	
class ThiSinh:
    def __init__(self, name, thuchanh, lythuyet, i) -> None:
        self.name = name
        self.ma = "TS0" + str(i)
        self.trungbinh = Decimal(0)

        if (len(thuchanh) == 2 and thuchanh != '10' )or thuchanh == "100" :
            self.trungbinh += (Decimal(thuchanh) / 10)
        else:
            self.trungbinh += Decimal(thuchanh)
        if (len(lythuyet) == 2 and lythuyet != '10') or lythuyet == "100":
             self.trungbinh += (Decimal(lythuyet) / 10)
        else:
            self.trungbinh += Decimal(lythuyet)
        self.trungbinh = Decimal(self.trungbinh) / 2
        self.loai = ""
        if self.trungbinh < 5:
            self.loai = "TRUOT"
        if self.trungbinh >= 5 and self.trungbinh < 8:
             self.loai = "CAN NHAC"
        if self.trungbinh >= 8 and self.trungbinh <= 9.5:
            self.loai = "DAT"
        if self.trungbinh > 9.5:
            self.loai = "XUAT SAC"
        pass
    def __str__(self) -> str:
          return "{} {} {} {}".format(self.ma, self.name, self.trungbinh.quantize(Decimal('0.00'), rounding= ROUND_HALF_UP), self.loai)
n = int(input())
a = []
for i in range(n):
    a.append(ThiSinh(input(), input(), input(), i + 1))
a.sort(key = lambda x : -x.trungbinh)
