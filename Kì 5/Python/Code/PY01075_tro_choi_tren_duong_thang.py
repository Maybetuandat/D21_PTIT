from math import gcd
t = int (input())
while t > 0:
    t -= 1
    
    a = {0: 0}
    b = [0]
    n = int(input())
    l = list(map(int, input().split()))
    c = list(map(int, input().split()))

    for i in range(n):
        for p in b:
            d = gcd(p, l[i])
            chiphi = a[p] + c[i]
            if d not in a:
                a[d] = chiphi
                b.append(d)
            elif a[d] > chiphi:
                a[d] = chiphi
    if 1 not in a:
        a[1] = -1
    print(a[1])