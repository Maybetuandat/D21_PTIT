class Diem:
    def __init__(self,masv, ten , lop) -> None:
          self.masv = masv
          self.ten = ten
          self.lop = lop
          self.chuyencan = ""
    def setchuyencan(self, chuyencan):
          self.chuyencan = chuyencan
    def __str__(self) -> str:
          return "{} {} {} {}".format(self.masv, self.ten, self.lop, self.chuyencan)
          pass                                
a = []
