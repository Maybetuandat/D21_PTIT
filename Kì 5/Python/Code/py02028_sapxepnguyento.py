from math import *                       
nmax = 10009                            
isprime = [0] *(nmax + 5) 
isprime[0]  = 1
isprime[1] = 1
for i in range(2, int(sqrt(nmax))  + 1):
    if  isprime[i] == 0:
        for j in range(2, nmax // i + 1):
            isprime[i*j] = 1                       
n = int(input())
a = list(map(int, input().split()))
vs = [0]  * n
snt = []
for i in range(n):
    if isprime[a[i]] == 0:
        snt.append(a[i])
        vs[i] = 1

snt.sort()
index = 0
for i in range(n):
    if vs[i] == 0:
        print(a[i], end = " ")
    else:
        print(snt[index], end = " ")
        index += 1
