def countConsecutive(N):
     
    # ta sẽ xem dãy số bắt đầu là a, từ các số sau sẽ là a+1, a+2, ..., a + L
    # n= a + a +1 + a + 2 + .. + a + L
    # n = (l + 1) * a + (1 + 2 + . ... + l)
    # n = (l + 1) * a + (l +1) * l / 2
    # if a == int((n - (l+1) * l // 2) / (l + 1))
    count = 0
    L = 1
    while( L * (L + 1) < 2 * N):
        a = (1.0 * N - (L * (L + 1) ) / 2) / (L + 1)
        if (a - int(a) == 0.0):
            count += 1
        L += 1
    return count
 
