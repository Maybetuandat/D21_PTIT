from math import*                                
nmax = int(1e6 )
isprime = [0] * (nmax + 9)
isprime[0] = 1
isprime[1] = 1
for i in range(2,  int(sqrt(nmax)) + 1 ):
    if isprime[i] == 0:
        for j in range(2, nmax // i + 1):
            isprime[i*j] = 1
def check(n):
    for i in range(2,int(sqrt(n)) + 1):
        if n % i == 0 and isprime[i] == 0:
                k = n // i
                if isprime[k] == 0 and k != i:
                     return True
    return False
n = int(input())
dem = 0
for i in range(4, int(sqrt(n)) + 1):
    if check(i) == True:
    
        dem += 1
for i in range(2, int(n ** 0.125) + 1):
     if i ** 8 <= n and isprime[i] == 0 :
    
          dem += 1
print(dem)