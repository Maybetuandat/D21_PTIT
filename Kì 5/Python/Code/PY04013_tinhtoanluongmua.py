def convert(s):
    return (int(s[0:2]) * 60 + int(s[3:len(s)]))
class Tram:
       def __init__(self, ten , timestart, timeend, luongmua, i) -> None:
              self.ten = ten
              self.timestart = timestart
              self.timeend = timeend
              self.luongmua = luongmua
              self.thoigian = convert(timeend) - convert(timestart)
              self.ma = "T" + str(i).zfill(2)
              
              pass       
       def setthoigianvaluongmua(self, begin, end, add):
             self.thoigian += (convert(end) - convert(begin))
             self.luongmua += add
             
       def __str__(self) -> str:
             muatrungbinh = self.luongmua  / (self.thoigian /  60)
             return "{} {} {:.2f}".format(self.ma, self.ten, muatrungbinh)                              
