def check(s):
    dem = 0
    for i in s:
        if i == '2':
            dem += 1    
    if dem >= (len(s) / 2):
        return True
    else:
        return False        
def xuli():
    n = int(input())
    dq = Queue()
    dq.put("1")
    dem = 0
    while dq.empty == False:
        s = dq.get()
        print(s)
        if dem == n:
            break
        dq.put(s + "0")
        dq.put(s + "1")
        dq.put(s + "2")


for _ in range(int(input())):
       xuli()