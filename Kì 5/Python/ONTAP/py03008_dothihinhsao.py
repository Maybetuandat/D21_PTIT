dem = [0] *(10** 5)
arr= []
arr = set(arr)
n = int(input())
for _ in range(n-1):
    x, y = map(int, input().split())
    dem[x] +=1
    dem[y] += 1
    arr.add(x)
    arr.add(y)
check = False
for x in arr:
    if dem[x] == n - 1:
        print('Yes')
        check = True
        break
if check == False:
    print('No')