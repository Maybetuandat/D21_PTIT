from datetime import *
d = dict()           
class LichThi:
    def __init__(self, mamon, tenmon, ngaythi, nhom, i) -> None:
        self.macathi = 'T' + str(i).zfill(3)
        self.mamon = mamon
        self.tenmon = tenmon
        self.ngaythi = ngaythi
        self.timecompare = datetime.strptime(ngaythi, '%d/%m/%Y %H:%M')
        self.nhom = nhom
        pass
    def __str__(self) -> str:
        return '{} {} {} {} {}'.format(self.macathi, self.mamon, self.tenmon, self.ngaythi, self.nhom)

n , m = map(int, input().split())
for _ in range(n):
    key = input()
    val = input()
    d[key] = val
a = []
for i in range(1, m + 1):
    s = input().strip().split()
    a.append(LichThi(s[0], d[s[0]],s[1] + " " + s[2], s[3], i ))
a.sort(key = lambda x : (x.timecompare, x.mamon))
for x in a:
    print(x)