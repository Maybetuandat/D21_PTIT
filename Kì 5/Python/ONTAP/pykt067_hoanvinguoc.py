res = []    
def sinh(s, n, dem, vs):
    if dem == n:
        res.append(s)
        return
    for i in range(1, n + 1):
         if vs[i] ==0 :
              vs[i] = 1
              sinh(s + str(i), n, dem + 1, vs)
              vs[i] = 0                      
def xuli():
    n = int(input())
    vs = [0] * 100
    res.clear()
    sinh("", n , 0, vs)
    print(len(res))
    for i in range(len(res) - 1, -1, -1):
        print(res[i], end = " ")
    print()
for _ in range(int(input())):
       xuli()