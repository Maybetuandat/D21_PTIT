from decimal import *                           
class BangDiem:
    def __init__(self, ten, s,i) -> None:
        self.ten = ten
        self.mahs = 'HS' + str(i).zfill(2)
        self.diemtrungbinh = Decimal(0)
        self.diemtrungbinh += (s[0] * 2 +  s[1] * 2)
        for i in range(2, 10):
            self.diemtrungbinh += s[i]
        self.diemtrungbinh =(self.diemtrungbinh/ Decimal(12)).quantize(Decimal('0.0'), rounding = ROUND_HALF_UP)
        if self.diemtrungbinh >= 9:
            self.loai = 'XUAT SAC'
        if self.diemtrungbinh >= 8 and self.diemtrungbinh <= 8.9:
            self.loai = 'GIOI'
        if self.diemtrungbinh >= 7 and self.diemtrungbinh <= 7.9:
            self.loai = 'KHA'
        if self.diemtrungbinh >= 5 and self.diemtrungbinh <= 6.9:
            self.loai = 'TB'
        if self.diemtrungbinh < 5 :
            self.loai = 'YEU'
    def __str__(self) -> str:
        return '{} {} {} {}'.format(self.mahs, self.ten, self.diemtrungbinh, self.loai)
a = []
for i in range(int(input())):
    a.append(BangDiem(input(), list(map(Decimal, input().split())), i + 1))
a.sort(key = lambda x : -x.diemtrungbinh)
for x in a:
    print(x)