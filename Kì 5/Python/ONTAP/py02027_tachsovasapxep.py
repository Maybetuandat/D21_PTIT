arr = []   
check='0123456789'                             
def xuli():
    s = input()
    i = 0
    while i < len(s):
        if s[i] in check:
            j = i + 1
            while j < len(s) and s[j] in check:
                j += 1
            arr.append(int(s[i:j]))
            i = j
        else:
             i += 1             
for _ in range(int(input())):
       xuli()
arr.sort()
for x in arr:
    print(x)