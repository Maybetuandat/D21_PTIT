giatien =dict()
giatien['Xe_con 5'] = 10000
giatien['Xe_con 7'] = 15000
giatien['Xe_tai 2'] = 20000
giatien['Xe_khach 29'] = 50000
giatien['Xe_khach 45'] = 70000
d = dict()
for _ in range(int(input())):
    s = list(input().split())
    if d.get(s[4]) != None:
        if s[3] == 'IN':
           d[s[4]] += giatien[s[1] + ' '+ s[2]]
    else:
        if s[3] == 'IN':
            d[s[4]] = giatien[s[1] + ' ' + s[2]]
for x in d:
    print('{}: {}'.format(x, d[x]))
