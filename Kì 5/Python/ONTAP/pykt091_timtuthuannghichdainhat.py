d =dict()
res = []
a = []
f = open('VANBAN.in', 'r')
#f = open('D:\Exercise\Kì 5\Python\ONTAP\VANBAN.in', 'r')
maxx = 0
for x in f.readlines():
    l = list(x.strip().split())
    a += l
    for i in l:
        if d.get(i) == None:
            d[i] = 1
        else:
            d[i] += 1
        if i == i[::-1] and len(i) > maxx:
            maxx = len(i)
for x in a:
    if x== x[::-1] and len(x) == maxx and x not in res:
        res.append(x)
for x in res:
    print(x, d[x])

    
