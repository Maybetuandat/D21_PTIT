from datetime import *                      
class Phim:
    def __init__(self, theloai, ngaychieu, tenphim, sotap, i) -> None:
        self.theloai = theloai
        self.maphim = 'P' + str(i).zfill(3)
        self.ngaychieu = ngaychieu
        self.timecompare = datetime.strptime(ngaychieu, '%d/%m/%Y')
        self.tenphim = tenphim
        self.sotap = sotap
    def __str__(self) -> str:
        return '{} {} {} {} {}'.format(self.maphim, self.theloai, self.ngaychieu, self.tenphim, self.sotap)
    
n , m = map(int, input().split())
d = dict()
for i in range(n):
    key = 'TL' + str(i+ 1).zfill(3)
    d[key] = input()
a = []
for i in range(m):
    key = input()
    a.append(Phim(d[key], input(), input(), input(), i + 1))
a.sort(key = lambda x : (x.timecompare, x.tenphim, x.sotap))
for x in a:
    print(x)