            #Sử dụng thuật toán kahn để kiểm tra xem thứ tự sắp xếp toppo có dủ không, nếu nó đủ số đỉnh thì ko thể có chu trình
            #còn nếu nó không đủ số đỉnh thì có thể tạo chu trình
        #bản chất bài toàn là possible thì ko được có chu trình, còn impossible là sẽ có chu trình: đơn cử như ví dụ đề ra 
        # an -> bình, bình -> cong, nếu cong -> an : phép toán này hoàn toàn sai(trường hợp tồn tại chu trình)
        # an -> bình, binh -> cong, an -> cong: phep này không tạo ra chu trình, va no co the đúng(possible)
def xuli():
    # su dung 1 dictionary de luu lai cac dinh ke tiep
    # 1 dictionary de luu so luong ban bac vao
    Isnext = dict()
    banbacvao = dict()
    st = set()
    for _ in range(int(input())):
        s = input().split()
        st.add(s[0])
        st.add(s[2])
        if s[1] == '>':
            # ta se xem la dinh di tu s[0] den s[2]
            if Isnext.get(s[0]) == None:
                Isnext[s[0]] = list()
            Isnext[s[0]].append(s[2])
            if banbacvao.get(s[2]) == None:
                banbacvao[s[2]] = 1
            else:
                banbacvao[s[2]] += 1
        else:
            # ta se xem la dinh di tu s[2] den s[0]
            if Isnext.get(s[2]) == None:
                Isnext[s[2]] = list()
            Isnext[s[2]].append(s[0])
            if banbacvao.get(s[0]) == None:
                banbacvao[s[0]] = 1
            else:
                banbacvao[s[0]] += 1
    dq = deque()
    cnt = 0
    for x in st:
        if banbacvao.get(x) == None:
            dq.append(x)
    while len(dq) > 0:
        top = dq.popleft()
        cnt += 1
        if Isnext.get(top) != None:
            for x in Isnext[top]:
                if banbacvao.get(x) != None:
                    banbacvao[x] -= 1
                    if banbacvao[x] == 0:
                        dq.append(x)
            
    if cnt != len(st):
        print('impossible')
    else:
        print('possible')

xuli()   