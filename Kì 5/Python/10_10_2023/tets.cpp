bool  check(string s)
{
    if(s[0] == '0')
    return false;
    int n = s.length();
    for(int i=0; i<n / 2; i++)
    {
        if(s[i] != s[n - i - 1])
          return false;
    }
    return true;
}
ll  convert(string s)
{
    ll tong = 0;
    for(char ch : s)
      tong = tong * 10 + (ch - '0');
     return tong;
}
