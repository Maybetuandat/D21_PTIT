class ThiSinh:
       def __init__(self, hoten, diemthi, dantoc, khuvuc, i) -> None:
              self.hoten = hoten
              self.diemthi = diemthi
              self.dantoc = dantoc
              self.khuvuc = khuvuc
              self.mats = "TS%.2d" %i
              self.uutien = 0.0
              if self.khuvuc == 1:
                     self.uutien += 1.5
              
              elif self.khuvuc == 2:
                     self.uutien += 1.0
              if self.dantoc != 'Kinh':
                     self.uutien += 1.5 
              self.tongdiem  = self.diemthi +self.uutien
              if self.tongdiem >= 20.5:
                     self.trangthai = "Do"
              else:
                     self.trangthai = "Truot"
       def __str__(self) -> str:
              return self.mats+ " " + self.hoten + " %.1f" %self.tongdiem + " "+ self.trangthai
                                

ans = []
for i in range(1, int(input())  + 1):
       a = list(map(str,input().split()))
       cmp = ""
       for j in a:
              cmp = cmp + " "+ j.title()
       ans.append(ThiSinh(cmp.strip(), float(input()), input(), int(input()), i ))
       
ans.sort(key= lambda x :  (-1 * x.tongdiem, x.mats))
for i in ans:
       print(i)
