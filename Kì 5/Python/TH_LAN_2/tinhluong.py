def tinh(ma, sonam):
        if ma == 'A':
            if sonam <= 3:
                return  10
            elif  4 <= sonam <= 8:
                 return 12
            elif 9 <= sonam <= 15:
                return 14
            else: 
                return  20
        if ma == 'B':
            if sonam <= 3:
                return  10
            elif  4 <= sonam <= 8:
                 return 11
            elif 9 <= sonam <= 15:
                return 13
            else: 
                return  16
        if ma == 'C':
            if sonam <= 3:
                return  9
            elif  4 <= sonam <= 8:
                 return 10
            elif 9 <= sonam <= 15:
                return 12
            else: 
                return  14
        if ma == 'D':
            if sonam <= 3:
                return  8
            elif  4 <= sonam <= 8:
                 return 9
            elif 9 <= sonam <= 15:
                return 11
            else: 
                return  13
        return 0                   
