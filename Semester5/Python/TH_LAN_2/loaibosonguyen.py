#f = open('D:\Exercise\Kì 5\Python\TH_LAN_2\DATA.in')
f = open('DATA.in')
s = f.read()
a = list(map(str, s.split()))
ans = []
int_min = -2147483648
int_max = 2147483647
for i in a:
    try:
        x = int(i)
        if x < int_min or x > int_max:
            ans.append(i)
    except:
        ans.append(i)
ans.sort()
for i in ans:
    print(i, end = " ")
