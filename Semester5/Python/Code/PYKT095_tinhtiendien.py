d = dict()
d['A'] = 100
d['B'] = 500
d['C']  = 200
class TienDien:

    def __init__(self, ten, cmp, i) -> None:
        s = list(ten.lower().split())
        self.ten = ""
        self.ma = "KH" + str(i).zfill(2)

        for x in s:
            self.ten += (x[0].upper() + x[1:len(x)] + " ")
        self.ten = self.ten.strip()
        self.tongtien = 0
        arr= []
        arr = list(cmp.split())
        sodien = int(arr[2]) - int(arr[1])
        self.vuotdinhmuc = 0
        if sodien >=  d[arr[0]]:
           self.tientrongdinhmuc = d[arr[0]] * 450
           self.vuotdinhmuc = (sodien - d[arr[0]]) * 1000
        else:
            self.tientrongdinhmuc = sodien * 450
        self.vat = self.vuotdinhmuc // 20
        self.tongtien = self.tientrongdinhmuc + self.vuotdinhmuc + self.vat
    def __str__(self) -> str:
          return '{} {} {} {} {} {}'.format(self.ma, self.ten, self.tientrongdinhmuc, self.vuotdinhmuc, self.vat, self.tongtien)
a = []
for i in range(int(input())):
    a.append(TienDien(input(), input(), i + 1))
a.sort(key = lambda x : -x.tongtien)
for x in a:
    print(x)
        
    


