import math                            
class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y
    
    def distance ( self, other):
        d1 = other.x - self.x
        d2 = other.y - self.y
        a = math.sqrt( d1 * d1 + d2 * d2 )
        return a
class Triangle:
    def __init__ (self, Point1, Point2, Point3):
        self.Point1 = Point1
        self.Point2 = Point2
        self.Point3 = Point3
    
    def __str__(self) :
        c = self.Point1.distance(self.Point2)
        b = self.Point2.distance(self.Point3)
        a = self.Point3.distance(self.Point1)
        if(max(a, b, c) * 2 >= a + b + c) : return 'INVALID'
        else :
            d = math.sqrt((a + b + c) * (a + b - c) * (a - b + c) * (-a + b + c)) / 4
            return '{:.2f}'.format(d)
