from datetime import *
from decimal import *
class Dua:
    def __init__(self, ten, donvi, thoigianden) -> None:
        self.ten = ten
        self.donvi = donvi
        
        self.ma = ""
        for x in self.donvi.split():
            self.ma += x[0]
        for x in self.ten.split():
            self.ma = self.ma + x[0]
        
        self.time = str(datetime.strptime(thoigianden, "%H:%M") - datetime.strptime("6:00", "%H:%M")).split()[0]
        self.vantoc = Decimal(0)
        a = list(self.time.split(':'))
        self.vantoc = Decimal(120) / Decimal(Decimal(a[0]) + Decimal(Decimal(a[1]) / Decimal(60)))
        self.vantoc = self.vantoc.quantize(Decimal("0"), rounding= ROUND_HALF_UP)
        pass                                
    def __str__(self) -> str:
          return '{} {} {} {} Km/h'.format(self.ma, self.ten, self.donvi, self.vantoc)
