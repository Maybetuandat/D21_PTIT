from decimal import *
class BangDiem:
    def __init__(self, ten , arr , i) -> None:
        self.arr = arr
        self.ten = ten
        self.ma = "HS" + str(i).zfill(2)
        self.diemtrungbinh = Decimal(0)
        self.diemtrungbinh += (self.arr[0] * 2 + self.arr[1] * 2)
        for i in range(2, len(self.arr)):
            self.diemtrungbinh += self.arr[i]
            
        self.diemtrungbinh = self.diemtrungbinh / 12
        self.diemtrungbinh = self.diemtrungbinh.quantize(Decimal('0.0'), rounding=ROUND_HALF_UP)
        #self.diemtrungbinh = round(self.diemtrungbinh, 1)
        if round(self.diemtrungbinh, 1) >= 9.0:
            self.loai = "XUAT SAC"
        if round(self.diemtrungbinh , 1) >= 8.0 and round(self.diemtrungbinh, 1) <= 8.9:
            self.loai = "GIOI"
        if round(self.diemtrungbinh , 1) >= 7.0 and round(self.diemtrungbinh, 1) <= 7.9:
            self.loai = "KHA"
        if round(self.diemtrungbinh , 1) >= 5.0 and round(self.diemtrungbinh, 1) <= 6.9:
            self.loai = "TB"
        if round(self.diemtrungbinh, 1) < 5 :
            self.loai = "YEU"    
    def __str__(self) -> str:
       
        getcontext().prec = 1
        return "{} {} {} {}".format(self.ma, self.ten, self.diemtrungbinh, self.loai)
        
n = int(input())
a = []
for i in range(n):
    a.append(BangDiem(input(), list(map(Decimal, input().split())), i + 1))
a.sort(key = lambda x : -x.diemtrungbinh)
for x in a:
    print(x)
    
