f = open('SOTAY.txt', 'r')
class DanhBa:
    def __init__(self, ten, sodth, day) -> None:
        self.ten = ten
        s = list(self.ten.split())
        self.tenchinh = s[-1]
        self.hodem = ""
        for i in range(len(s) - 1):
            self.hodem += s[i]
        self.hodem = self.hodem.strip()
        self.sodth = sodth
        self.day = day
        pass
    def __str__(self) -> str:
          return '{}: {} {}'.format(self.ten, self.sodth, self.day)
    
