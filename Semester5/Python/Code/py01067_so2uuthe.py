# su dung hang doi queue              
import queue                           
def xuli():
    n = int(input())
    dq = queue.Queue()
    dq.put("1")
    dq.put("2")
    dem = 0
    while dq.qsize():
        s = dq.get()
    
      #  print(s)
        if s.count("2") > len(s) / 2:
             dem += 1
             print(s, end = " ")
        dq.put(s + "0")
        dq.put(s + "1")
        dq.put(s + "2")
        if dem == n:
             break
    print()   
   

for _ in range(int(input())):
       xuli()