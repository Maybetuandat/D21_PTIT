def xuli():
    s = list(input().split())
    cmp = ""
    for x in s:
        cmp1 = cmp + x +  " "
        if len(cmp1) == 100:
             return cmp1.strip()
        elif len(cmp1) > 100:
               return cmp.strip()
        else:
             cmp = cmp1
    return cmp
             

    
for _ in range(int(input())):
       print(xuli())