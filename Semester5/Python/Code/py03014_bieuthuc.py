def xuli():
   s = input()
   checkdong = [0] * 1000009
   demmo = 0
   for i in s:
        if i == '(':
             demmo += 1
             print(demmo, end = " ")
        elif i == ')':
            demdong = demmo
            while demdong > 0 and checkdong[demdong] == 1:
                  demdong-= 1
            checkdong[demdong] = 1
            print(demdong, end = " ")
        else:
             continue
   print()
            
             
             
for _ in range(int(input())):
       xuli()