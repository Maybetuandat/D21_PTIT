import re
s = ""    
while True:
    try: 
        s += input()
    except EOFError:
        break
a = list(map(str, re.split('[?!.]', s)))
for i in a:
    x= i.strip().lower().split()
    if len(x) == 0:
        break
    print(x[0].title(), end = " ")
    for j in range(1, len(x)):
        print(x[j], end = " ")
    print()
