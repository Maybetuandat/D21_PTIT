from decimal import *                    
class SinhVien:
    def __init__(self, ten, diem1, diem2, diem3, i) -> None:
        s = list(ten.lower().split())
        self.ten = ""
        for x in s:
            self.ten += x[0].upper() + x[1:len(x)] + " "
        self.ten = self.ten.strip()
        self.trungbinh = (Decimal(diem1) * 3 + Decimal(diem2) * 3 + Decimal(diem3) * 2) / 8
        self.trungbinh = self.trungbinh.quantize(Decimal('0.00'), rounding= ROUND_HALF_UP)
        self.masv = "SV" + str(i).zfill(2)
        self.thuhang = 0
    def setthuhang(self, x):
        self.thuhang = x
    def __str__(self) -> str:
        return "{} {} {} {}".format(self.masv, self.ten, self.trungbinh, self.thuhang)
a = []
for i in range(int(input())):
    a.append(SinhVien(input(), input(), input(), input(), i + 1))
a.sort(key= lambda x : -x.trungbinh)
i  = 0
thuhang  = 1
while i < len(a):
    j = i + 1
    if j == len(a):
        a[i].setthuhang(thuhang)
    while j < len(a) and a[j].trungbinh == a[i].trungbinh :
        j +=1
    for t in range(i, j):
        a[t].setthuhang(thuhang)
    thuhang += j - i
    i = j
for x in a :
    print(x)