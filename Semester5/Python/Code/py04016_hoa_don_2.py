from datetime import *     
tien = [25, 34, 50, 80]       
class HoaDon:
    def __init__(self, name, phong, ngayden, ngaydi, phatsinh, i ) -> None:
          self.name = name
          self.makh = "KH" + str(i).zfill(2)
          self.phong = phong
          tang = int(self.phong[0])
          self.tongtien = phatsinh
          self.ngayo = str(datetime.strptime(ngaydi, '%d/%m/%Y') - datetime.strptime(ngayden, '%d/%m/%Y')).split()[0]
          if self.ngayo == '0:00:00':
               self.songayo = 1
          else:
               self.songayo = int(self.ngayo) + 1
          
          self.tongtien += (tien[tang - 1]    * self.songayo)
          
          pass                            
    def __str__(self) -> str:
        return "{} {} {} {} {}".format(self.makh, self.name, self.phong, self.songayo, self.tongtien)  
        
a = []
for i in range(int(input())):
    a.append(HoaDon(input().strip(), input().strip(), input().strip(), input().strip(), int(input()), i + 1))
a.sort(key = lambda x : -x.tongtien)
for x in a:
     print(x)