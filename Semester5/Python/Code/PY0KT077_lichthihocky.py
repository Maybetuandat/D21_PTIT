from datetime import*                            
d = dict()
n, m = map(int, input().split())
for _ in range(n):
    key = input().strip()
    val = input().strip()
    d[key] = val
class CaThi:
    def __init__(self, mamon, Time, nhomthi, i) -> None:
        self.mamon = mamon
        self.macathi = "T" + str(i).zfill(3)
        self.tenmon = d[mamon]
        self.Time = Time
        self.thoigian  = datetime.strptime(Time, "%d/%m/%Y %H:%M")
        self.nhomthi = nhomthi
    def __str__(self) -> str:
        return "{} {} {} {} {}".format(self.macathi, self.mamon, self.tenmon,self.Time, self.nhomthi ) 
a = []
for i in range(m):
    s = list(input().split())
    a.append(CaThi(s[0], s[1] +" "+  s[2], s[3], i + 1))
a.sort(key = lambda x : (x.thoigian, x.mamon))
for x in a:
    print(x)
