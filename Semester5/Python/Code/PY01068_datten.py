n , m = map(int, input().split())
se = sorted(set(list(input().split())))
ans = []
for x in se :
    ans.append(x)
n = len(ans)
a = [0] * (m + 1)
for i in range(1, m + 1):
    a[i] = i 
while True:
    for i in  range(1 , m + 1):
        print(ans[a[i] - 1], end = " ")
    print()
    i =  m 
    while i > 0 and a[i] >= n - m + i:
        i -= 1
    if i == 0:
        break
    a[i] += 1
    # if  i == m:
    #     continue
    for j in range (i + 1, m + 1):
        a[j] = a[i] + j - i
