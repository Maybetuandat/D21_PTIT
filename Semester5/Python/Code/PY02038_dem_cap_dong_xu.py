n = int(input())
a = []
sum = 0
for i in range(n):
    s = input()
    dem1 = s.count('C')
    sum += (dem1 - 1 ) * dem1 // 2
    a.append(s)
m = len(a[0])
for i in range(m):
    dem = 0
    for j in range(n):
        if a[j][i] == 'C':
            dem += 1
    sum += ((dem - 1) * dem) // 2
print(sum)