def xuli():
   n  = int(input())
   dem = [0] * int(1e6 + 9)
   minn = int(1e9+ 9)
   maxx = 0
   for i in  map(int, input().split()):
        dem[i] += 1
        minn = min(minn, i)
        maxx = max(maxx, i)
   for i in range(minn, maxx + 1):
        if dem[i] % 2 == 1:
             print(i)
             return
    

for _ in range(int(input())):
       xuli()