class HoaDon:
    def __init__(self, ten, socu, somoi, i) -> None:
        self.ten = ten
        cnt = somoi - socu

        self.makh = "KH" +str(i).zfill(2) 
    
        if cnt <= 50:
            self.sotien = cnt * 100
            self.sotien = (self.sotien * 102) / 100
        if cnt >= 51 and cnt <= 100:
            self.sotien = 50 * 100
            self.sotien += (cnt - 50) * 150
            self.sotien = (self.sotien * 103) / 100
        if cnt > 100:
            self.sotien = 50 * 250
            self.sotien += (cnt - 100) * 200
            self.sotien = (self.sotien * 105) / 100
        self.sotien = round(self.sotien)
        pass 
    def __str__(self) -> str:
        return "{} {} {}".format(self.makh, self.ten, self.sotien)
        pass
