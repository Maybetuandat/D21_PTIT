def xuli():
   n, m = map(int, input().split())
   a = []
   for i in range(n):
        a.append(list(map(int, input().split())))
   ans = [[0] * n for _ in range(n)]
   b = []
   for i in range(m):
        x  = []
        for  j in range(n):
             x.append(a[j][i])
        b.append(x)
#    for i in range(m):
#         for j in range(n):
#              print(b[i][j], end = " ")
#         print()
   for i in range(n):
        for j in range(n):
             for k in range(m):
                  ans[i][j] += a[i][k] * b[k][j]

   for i in range(n):
        for j in range(n):
             print(ans[i][j], end = " ")
        print()


for _ in range(int(input())):
       xuli()