N = int(input())
students = []


for _ in range(N):
    name = input()
    correct, submit = map(int, input().split())
    students.append((name, (correct, submit)))


students.sort(key= lambda x : (-1 * x[1][0], (x[1][1]), x[0]))

for i in students:
    print(f"{i[0]} {i[1][0]} {i[1][1]}")