class XemPhim:
    def __init__(self,matheloai, ngaychieu, tenphim, sotap, theloai, index) -> None:
            self.matheloai = matheloai
            self.ngaychieu = ngaychieu
            self.tenphim = tenphim
            self.sotap = sotap
            self.maphim =  "P" + str(index).zfill(3)
            self.theloai = theloai
            a = list(self.ngaychieu.split("/"))
            self.day = int(a[0])
            self.month = int(a[1])
            self.year = int(a[2])



diction = dict()   
a = []                  
n ,  m = map(int, input().split())
for i in range(1, n + 1):
    s = input()
    key = "TL" + str(i).zfill(3)
    diction[key] = s

for i in range(m):
    matheloai  = input()
    ngaychieu= input()
    tenphim = input()
    sotap = input()

    a.append(XemPhim(matheloai, ngaychieu, tenphim, sotap, diction.get(matheloai), i + 1))
a.sort(key = lambda x : (x.year, x.month, x.day, x.sotap))
for x in a:
    print(x.maphim, end = " ")
    print(x.theloai, end = " ")
    print(x.ngaychieu, end = " ")
    print(x.tenphim, end = " ")
    print(x.sotap, end = " ")
    print()