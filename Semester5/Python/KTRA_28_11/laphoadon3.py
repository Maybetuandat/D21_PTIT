class MatHang:
    def __init__(self, mamh, tenmh, soluong, dongia, chietkhau) -> None:
        self.mamh = mamh
        self.tenmh = tenmh
        self.soluong = soluong
        self.dongia = dongia
        self.chietkhau = chietkhau
        self.thanhtoan = self.dongia * self.soluong- self.chietkhau

   
n = int(input())
ds = []
for _ in range(n):
    a = MatHang(input(), input(), int(input()), int(input()), int(input()))
    ds.append(a)
ds.sort(key = lambda x : -x.thanhtoan)
for x in ds:
    print(x.mamh, end = " ")
    print(x.tenmh, end = " ")
    print(x.soluong, end = " ")
    print(x.dongia, end = " ")
    print(x.chietkhau, end = " ")
    print(x.thanhtoan, end = " ")
    print()