class data:
    def __init__(self, giadau, giatang, chenhlech) -> None:
        self.giadau = giadau
        self.giatang = giatang
        self.chenhlech = chenhlech
   
n , k = map(int, input().split())
a = list(map(int, input().split()))
b = list(map(int, input().split()))
c = []
for i in range(n):
    c.append(data(a[i], b[i], a[i] - b[i]))
c.sort(key = lambda x : (x.chenhlech, x.giadau))
sum = 0
for i in range(k):
    sum += c[i].giadau
for i in range(k, n):
    if c[i].giatang > c[i].giadau:
        sum += c[i].giadau
    else:
        sum += c[i].giatang
print(sum)