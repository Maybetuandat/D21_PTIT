def xuli(n, d):
    p = 1
    ans = 0
    l = n
    r = 0
    while l > 0:
        x = l %  10
        l //= 10
        ans = ans + int(l * p)

        if x == d:
            ans += r +  1
        elif x > d:
            ans += p
        p *= 10
        r = n % p

    if d == 0:
        ans -= (p-1) // 9
    return ans
t = int(input())
while t > 0:
    t -= 1
    a, b = map(int, input().split())
    if a > b:
        a , b = b , a
    cnt = [0] * 10
    for i in range(0, 10):
        cnt[i] = xuli(b, i)
        if a - 1 > 0:
            cnt[i] -= xuli(a-1, i)

    for i in range(0, 10):
        print(cnt[i], end = " ")
    print()
        