def timmax(a, b, c , d):
    ans = a
    ans = max(b, ans)                                
    ans = max(c, ans)
    ans = max(d, ans)
    return ans
n = int(input())
a = list(map(int, input().split()))
a.sort()
print(timmax(a[0] * a[1], a[n-1] * a[n-2], a[0] * a[1] * a[n-1] , a[n-1] * a[n-2] * a[n-3]))