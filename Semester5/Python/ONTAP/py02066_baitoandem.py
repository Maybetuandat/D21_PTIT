n = int(input())
a= []
maxx = 0
while len(a) < n:
    x = list(map(int, input().split()))
    a += x
maxx = max(a)
dem = [0] * (maxx + 1)
for x in a:
    dem[x] += 1
ok = False
for i in range(1, maxx + 1):
    if dem[i] == 0:
        ok = True
        print(i)
if ok == False:
    print('Excellent!')
