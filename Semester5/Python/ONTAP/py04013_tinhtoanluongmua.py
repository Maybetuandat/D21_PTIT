from decimal import*                            
def convert(s):
       return 60 * int(s[0:2]) +  int(s[3: 5])
class Mua:
       def __init__(self, ten , batdau, ketthuc, luongmua, i) -> None:
              self.ten = ten
              self.thoigian = convert(ketthuc) - convert(batdau)
              self.ma = 'T' + str(i).zfill(2)
              self.luongmua = luongmua
       def add(self,  start, end, luongmua):
              self.thoigian += (convert(end) - convert(start))
              self.luongmua += luongmua
       def __str__(self) -> str:
              trungbinh = Decimal(self.luongmua) / (Decimal(self.thoigian)/ Decimal(60))
              trungbinh = trungbinh.quantize(Decimal('0.00'), rounding= ROUND_HALF_UP)
              return '{} {} {}'.format(self.ma, self.ten, trungbinh)
       
d = dict()
for i in range(int(input())):
       key = input()
       if key not in d:
              tmp = Mua(key, input(), input(), int(input()), i + 1)
              d[key] = tmp
       else:
              d[key].add(input(), input(), int(input()))
       
for x in d:
       print(d[x])