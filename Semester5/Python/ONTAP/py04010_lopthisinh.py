class ThiSinh:
        def __init__(self, ten, ngaysinh, diem1, diem2, diem3) -> None:
            self.ten = ten
            self.ngaysinh = ngaysinh
            self.tongdiem = diem1 + diem2 + diem3
        def __str__(self) -> str:
            return '{} {} {}'.format(self.ten, self.ngaysinh, f'%.1f' %self.tongdiem)
