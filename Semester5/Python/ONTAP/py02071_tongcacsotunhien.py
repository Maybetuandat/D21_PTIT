res = []          
def dq(pre, remain , sum, s, n):
    
    if sum == n :
    #add answer to res
        res.append('(' + s.strip() + ')')
        return
    for i in range(remain, 0  , -1):
        if pre < i:
            continue
        dq(i ,remain - i, sum + i,s + str(i) + " ", n)
        
def xuli():
    n = int(input())
    res.clear()
    dq( n, n , 0,"", n)
    print(len(res))
    for x in res:
         print(x, end = " ")
    print()
for _ in range(int(input())):
       xuli()