import re
d = dict()
for _ in range(int(input())):
    for x in re.split("[^a-z0-9]", input().lower()):
        if x != '':
            if d.get(x) != None:
                d[x] += 1
            else:
                d[x] = 1
ans = sorted(d, key= lambda x : (-d[x], x))
for x in ans:
    print(x, d[x])