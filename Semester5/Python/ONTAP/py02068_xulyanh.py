def xuli():
    n , m , l = map(int , input().split())
    a = []
    x = [0] * (m + 1)
    a.append(x)
    for i in range(n):
        x = [0] + list(map(int, input().split()))
        a.append(x)
   #mang a gom cac gia tri so duoc dua vao
    ans = []
    x = [0] * (m + 1)
    ans.append(x)
    for i in range(n):
        ans.append([0] * (m + 1))
        
    for i in range(1, n + 1):
        for j in range(1, m + 1):
            ans[i][j] = a[i][j]    
            ans[i][j] = ans[i][j] + ans[i][j - 1]
    for j in range(1, m + 1):
        for i in range(1, n + 1):
            ans[i][j] = ans[i][j] + ans[i-1][j]        
    for i in range(1, n - l + 2):
        for j in range(1, m - l + 2):
            sum  = ans[i + l - 1] [j + l - 1] - ans[i + l - 1] [j- 1] - ans[i-1][j + l - 1] + ans[i - 1][j - 1]
            print(sum // (l * l), end = " ")
        print()



for _ in range(int(input())):
       xuli()