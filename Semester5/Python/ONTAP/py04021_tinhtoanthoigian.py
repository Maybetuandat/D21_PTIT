def convert(s):
    return 60 * int(s[0:2]) + int(s[3:5])                                
class ThoiGian:
    def __init__(self, ma, ten, vao, ra) -> None:
        self.ma = ma
        self.ten = ten
        self.thoigianchoi = convert(ra) - convert(vao)
    def __str__(self) -> str:
        return '{} {} {} gio {} phut'.format(self.ma, self.ten, self.thoigianchoi // 60, self.thoigianchoi % 60)
a = []
for _ in range(int(input())):
    a.append(ThoiGian(input(), input(), input(), input()))
a.sort(key = lambda x : -x.thoigianchoi)
for x in a:
    print(x)